    <h1>Dhamma and Sinhala Language School of Canberra</h1>

    <h2 id="introduction">Rules, Guidelines and Responsibilities</h2>
    <p>These guidelines are designed to foster a safe, respectful, disciplined and supportive environment for all children, parents, teachers, the Dhamma school committee and venerable monks. They embody Buddhist values and promote the holistic development of children through Dhamma and Sinhala language education.</p>

    <h2 id="what-is-school">1. What Is the Dhamma and Sinhala Language School?</h2>
    <p>The Dhamma and Sinhala Language School of Canberra exists to serve the community by:</p>
    <ul>
      <li>
        <p>Teaching the Lord Buddha's Dhamma to children in an age-appropriate and practical way</p>
      </li>
      <li>
        <p>Teaching Sinhala language and cultural values</p>
      </li>
      <li>
        <p>Supporting children to develop good conduct, discipline, compassion and mindfulness</p>
      </li>
      <li>
        <p>Celebrating key cultural and religious events, fostering a strong sense of community, identity and shared Buddhist values among children and families</p>
      </li>
      <li>
        <p>Creating a respectful connection between children, parents, teachers and the temple while encouraging families to practice Buddhist values in daily life</p>
      </li>
    </ul>
    <p>The school operates within the temple environment and follows Buddhist principles of respect, compassion, discipline and harmony.</p>

