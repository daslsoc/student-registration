    <h2 id="appendix-a"><strong>Appendix A &ndash; Annual Concert Participation Guidelines</strong></h2>
    <p>The Annual Concert is a key cultural and community event of the Dhamma and Sinhala Language School of Canberra. Participation reflects a child&rsquo;s commitment to learning, discipline and respectful behaviour, supported by parents and families.</p>
    <p>1. Attendance and Eligibility</p>
    <ul>
      <li>
        <p>Regular attendance throughout the year is required for students to participate in the Annual Concert.</p>
      </li>
      <li>
        <p>Attendance records from <strong>Terms 1 to 3</strong> will be reviewed as part of eligibility.</p>
      </li>
      <li>
        <p>Priority will not be given to students who attend irregularly during the year and only attend in Term 4.</p>
      </li>
      <li>
        <p>Students are expected to maintain <strong>a minimum of 70% attendance</strong>, particularly in Sinhala Language classes.</p>
      </li>
    </ul>
    <p>2. Student Participation and Item Limits</p>
    <ul>
      <li>
        <p>All students may participate in <strong>up to three (3) concert items</strong>, including the <strong>compulsory group singing activity</strong>.</p>
      </li>
      <li>
        <p>Participation in additional items requires <strong>prior approval from the Dhamma School Committee</strong>.</p>
      </li>
      <li>
        <p>Selection for concert items is based on:</p>
        <ul>
          <li>
            <p>Attendance&nbsp;</p>
          </li>
          <li>
            <p>Student skills, abilities and suitability</p>
          </li>
          <li>
            <p>Behaviour and cooperation</p>
          </li>
          <li>
            <p>Commitment to rehearsals and preparation</p>
          </li>
        </ul>
      </li>
    </ul>
    <p>3. Behaviour Expectations</p>
    <p><strong>Students</strong></p>
    <ul>
      <li>
        <p>Students must follow instructions from teachers, item coordinators, venerable monks and volunteers at all times.</p>
      </li>
      <li>
        <p>Respectful behaviour is required during rehearsals and performances.</p>
      </li>
      <li>
        <p>Failure to meet behaviour expectations may result in removal from an item or withdrawal from concert participation.</p>
      </li>
    </ul>
    <p><strong>Parents</strong></p>
    <ul>
      <li>
        <p>Parents are expected to support rehearsal schedules and school decisions.</p>
      </li>
      <li>
        <p>Respectful communication with teachers, coordinators and committee members is required.</p>
      </li>
      <li>
        <p>Disruptive behaviour will not be tolerated.</p>
      </li>
      <li>
        <p>Constructive feedback is welcome.</p>
      </li>
    </ul>
    <p>4. Rehearsals and Venue Arrangements</p>
    <ul>
      <li>
        <p>All rehearsals will be conducted <strong>only at the temple premises</strong>.</p>
      </li>
      <li>
        <p>This approach minimises <strong>insurance, Work Health and Safety (WHS) and risk management issues</strong> associated with external venues.</p>
      </li>
      <li>
        <p>Parents are responsible for ensuring that children attend all relevant rehearsals punctually and consistently according to the communicated schedule, as the school does not have the resources to individually guide or follow up with each child for their assigned activities.</p>
      </li>
    </ul>
    <p>5. Costs and Financial Responsibilities</p>
    <ul>
      <li>
        <p>The Dhamma School will cover <strong>material costs</strong> related to concert activities.</p>
      </li>
      <li>
        <p>Parents are responsible for all <strong>individual costume-related expenses</strong>, including:</p>
        <ul>
          <li>
            <p>Costumes</p>
          </li>
          <li>
            <p>Accessories</p>
          </li>
          <li>
            <p>Any additional costs required for costume preparation</p>
          </li>
        </ul>
      </li>
    </ul>
    <p>6. Communication and Concerns</p>
    <ul>
      <li>
        <p>Parents who have concerns regarding:</p>
        <ul>
          <li>
            <p>Item selection</p>
          </li>
          <li>
            <p>Rehearsal commitments</p>
          </li>
          <li>
            <p>Concert arrangements<br> may directly approach the <strong>Dhamma School Committee</strong> for discussion and clarification.</p>
          </li>
        </ul>
      </li>
      <li>
        <p>Concerns should be raised respectfully and through appropriate channels.</p>
      </li>
    </ul>
    <p>7. Final Notes</p>
    <ul>
      <li>
        <p>Participation in the Annual Concert is a privilege and requires cooperation between students, parents, teachers and the committee.</p>
      </li>
      <li>
        <p>The Dhamma School Committee reserves the right to make final decisions in the best interest of fairness, safety and the overall quality of the concert.</p>
      </li>
    </ul>
